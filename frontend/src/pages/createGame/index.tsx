import { useFormik } from 'formik';
import type { NextPage } from 'next';
import Head from 'next/head';
import { useCallback, useState } from 'react';
import { useDropzone } from 'react-dropzone';

import {
  Main,
  ImageContainer,
  ContainerDescription,
  DescriptionContainer,
  CommentContainer,
} from './styles';

import Input from '../../components/Form/Input';
import Textarea from '../../components/Form/TextArea';
import { ContainerMain } from '../../theme/globalstyles';
import Button from '../../components/Form/Button';

const initialValues = {
  image: {
    url: '',
    file: null,
  },
  description: '',
  name: '',
};

const CreateGame: NextPage = () => {

  const formik = useFormik({
    initialValues,
    onSubmit(values){
      console.log(values)
    }
  });

  const onDrop = useCallback((acceptedFiles) => {
    const file = acceptedFiles[0];
    const imageUrl = URL.createObjectURL(file);
    formik.setFieldValue("image",{
      url: imageUrl,
      file,
    })
  }, []);
  const { getRootProps, getInputProps } = useDropzone({ onDrop });

  return (
    <Main onSubmit={formik.handleSubmit}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <ContainerMain>
        <ImageContainer {...getRootProps()} existImage={Boolean(formik.values.image.url)}>
          <input {...getInputProps()} />
          {!formik.values.image.url ? (
            <div>Adicione uma imagem</div>
          ) : (
            <img alt="32" src={formik.values.image.url} />
          )}
        </ImageContainer>
        <ContainerDescription>
          <div>
            <Input labelValue="Nome" input={{
              value:formik.values.name,
              onChange:formik.handleChange,
              name:"name"
            }}/>
          </div>
          <Textarea
            container={{ className: 'height' }}
            labelValue="Descrição"
            maxValue="100"
            input={{
              value:formik.values.description,
              onChange:formik.handleChange,
              name:"description"
            }}
          />
          <Button type="submit" onClick={()=>formik.handleSubmit()}>Cadastrar</Button>
        </ContainerDescription>
      </ContainerMain>
    </Main>
  );
};

export default CreateGame;
